    private void viewLoanComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewLoanComboBoxActionPerformed
        if(viewLoanComboBox.getSelectedItem().toString()!="Loan Number               Last Name               Loan Amount               Term               Interest rate               Total amount owed"){
            jTextArea1.setText("Loan Number               Last Name               Loan Amount               Term               Interest rate               Total amount owed");
        }
    
        switch(viewLoanComboBox.getSelectedItem().toString()){
            case "All Loans":
                for(int i=0; i<count; i++){
                    jTextArea1.append(loanList[i].toString());
                }
                break;
            case "All Business Loans":
                for(int i=0; i<count; i++){
                    if(loanList[i] instanceof BusinessLoan)
                        jTextArea1.append(loanList[i].toString());
                }
                break;
            case "All Personal Loans":
                for(int i=0; i<count; i++){
                    if(loanList[i] instanceof PersonalLoan)
                        jTextArea1.append(loanList[i].toString());
                }
                break;
            case "A Particular Loan":
                String particularLoanNumber = jOptionPane1.showInputDialog(null, "Enter a loan number");
                for(int i=0; i<count; i++){
                    if(loanList[i].getLoanNumber() == Integer.parseInt(particularLoanNumber))
                        jTextArea1.append(loanList[i].toString());
                }
                break;
                
        }
    }//GEN-LAST:event_viewLoanComboBoxActionPerformed

