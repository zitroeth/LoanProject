    public int termStringToInt(){
        switch (termComboBox.getSelectedItem().toString()){
            case "1 Year":
                return 1;
            case "3 Years":
                return 3;
            case "5 Years":
                return 5;     
        }
        return 0;
    }
    
    public boolean checkExceptions(){
        
        try{
            Integer.parseInt(loanNumberTxt.getText());
            Double.parseDouble(loanAmountTxt.getText()); 
            Double.parseDouble(primeRateTxt.getText());
        }catch(NumberFormatException ic){
                exceptionPane.showMessageDialog(null, "Please enter valid input/s", "Alert", exceptionPane.WARNING_MESSAGE);
                return false;
        }
        
        for(int i=0; i<count; i++){
            if(Integer.parseInt(loanNumberTxt.getText())==loanList[i].getLoanNumber()){
                exceptionPane.showMessageDialog(null, "Loan Number Already Exists!", "Alert", exceptionPane.WARNING_MESSAGE);
                return false;
            }
        }
        

        if(Double.parseDouble(loanAmountTxt.getText())==0 || Double.parseDouble(loanAmountTxt.getText())>MAX_LOAN_AMOUNT){
            exceptionPane.showMessageDialog(null, "Please enter a valid loan amount", "Alert", exceptionPane.WARNING_MESSAGE);
            return false;
        }
        
        
        return true;
    }
    
    
